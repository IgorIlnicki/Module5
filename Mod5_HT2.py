
def my_generator(n):
 
    # Инициализируем счетчик
    value = 0
 
    # Цикл выполняется до тех пор, пока счетчик не станет меньше n
    while value < n:
 
        # Возвращаем текущее значение счетчика
        yield value
 
        # Увеличиваем счетчик
        value += 1
 
# Выполняем итерацию генератора
for value in my_generator(3):
 
    # Выводим каждое значение, полученное от генератора
    print(value)